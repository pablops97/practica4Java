    private DefaultListModel modeloLista()
    {
        
        DefaultListModel<String> modelo = new DefaultListModel<>();
        Node temp = vistaPrincipal.lista.getFirstNode();
        while(temp != null){
            if (temp.getMain() instanceof Empleado)
                {
                    
                    String infoObjeto = "Informaci√≥n del objeto: " + temp.getMain().toString();
                    modelo.addElement(infoObjeto);  

                }
                
            else
                modelo.addElement("No hay elementos");
            
            temp = temp.getNextNode();
        }
        
        
        return modelo;
    }


