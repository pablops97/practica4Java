    Empleado aux = (Empleado) vistaPrincipal.lista.getFirstNode().getMain();
    vistaPrincipal.botones.getBotonCalcular().setEnabled(activarCalculo(aux));
    tipoField.setText(vistaPrincipal.lista.getActualNode().getMain().getClass().getSimpleName());
    numeroField.setText(""+aux.getNumero());
    nombreField.setText(aux.getNombre());
    sueldoField.setText(""+aux.getSueldo());
    sueldoMaxField.setText(""+aux.getSueldoMaximo());
    fechaField.setText(aux.getFechaAlta().toString());
    diferenciarTipo();
