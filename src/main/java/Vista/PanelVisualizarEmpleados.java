public void diferenciarTipo(){
    Empleado aux = (Empleado)vistaPrincipal.lista.getActualNode().getMain();
    //aux.getClass().getSimpleName().contains("Analista")
    if(aux instanceof Analista){
        Analista analista = (Analista)aux;
        labelSustitutivo1.setText("Plus anual");
        labelSustitutivo2.setText("Horas trabajadas");
        fieldSustitutivo1.setText("" + analista.getPlusAnual());
        fieldSustitutivo2.setText("" + analista.getHorasTrabajadas());
    }else{
        Programador programador = (Programador)aux;
        labelSustitutivo1.setText("Sueldo extra mensual");
        labelSustitutivo2.setText("Lenguaje programación favorita");
        fieldSustitutivo1.setText("" + programador.getSueldoExtraMensual());
        fieldSustitutivo2.setText(programador.getLenguajeFavorito());    
    }
}

